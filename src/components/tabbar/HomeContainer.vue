  created() {
    this.getLunbotu();
  },
  methods: {
    getLunbotu() {
      // 获取轮播图数据的方法
      // 配置了全局的请求根路径，所有的请求后面也不用写了 
      this.$http.get("/api/getlunbo").then(result => {
        // 一定注意这里，我们换了axios的库，result不是body属性了，而是data属性
        // 我们在响应拦截器里面做了配置，返回的就是data数据，直接使用即可
        // console.log(result);
